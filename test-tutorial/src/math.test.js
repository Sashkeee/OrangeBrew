import { describe, it, expect } from 'vitest';
import { add, isEven } from './math';

// describe — это "папка" для наших тестов. Она группирует тесты по смыслу.
describe('Математические функции: add()', () => {

    // БАЗОВЫЕ СЛУЧАИ —————————————————————————————

    it('2 + 2 должно быть 4', () => {
        // expect(результат).toBe(ожидаемое_значение)
        expect(add(2, 2)).toBe(4);
    });

    it('1 + 0 должно быть 1 (ноль — нейтральный элемент)', () => {
        expect(add(1, 0)).toBe(1);
    });

    // ГРАНИЧНЫЕ СЛУЧАИ (Edge Cases) ——————————————
    // Это важный приём в тестировании — проверять "края" значений!

    it('отрицательные числа: -5 + -3 = -8', () => {
        expect(add(-5, -3)).toBe(-8);
    });

    it('смешанные числа: -5 + 10 = 5', () => {
        expect(add(-5, 10)).toBe(5);
    });

    it('дробные числа: 0.1 + 0.2 ≈ 0.3 (Осторожно с float!)', () => {
        // toBeCloseTo вместо toBe — потому что 0.1 + 0.2 = 0.30000000000000004 в JS!
        expect(add(0.1, 0.2)).toBeCloseTo(0.3);
    });

});

describe('Математические функции: isEven()', () => {

    // БАЗОВЫЕ СЛУЧАИ —————————————————————————————

    it('4 — это чётное число', () => {
        // toBe(true) — функция вернула правду
        expect(isEven(4)).toBe(true);
    });

    it('7 — это нечётное число', () => {
        // toBe(false) — функция вернула ложь
        expect(isEven(7)).toBe(false);
    });

    // ГРАНИЧНЫЕ СЛУЧАИ ————————————————————————————

    it('0 — это чётное число (делится на 2 без остатка)', () => {
        expect(isEven(0)).toBe(true);
    });

    it('1 — нечётное число', () => {
        expect(isEven(1)).toBe(false);
    });

    it('отрицательное чётное: -6 является чётным', () => {
        expect(isEven(-6)).toBe(true);
    });

    it('отрицательное нечётное: -3 является нечётным', () => {
        expect(isEven(-3)).toBe(false);
    });

});
